	/**
	 * Fetch stop words map for a language.
	 *
	 * @param string $language Language code (lowercase).
	 * @return array<string,bool> Map of stop words for O(1) lookups.
	 */
	public static function stop_words( string $language ): array {
		$list = self::list_for( strtolower( $language ) );
		if ( empty( $list ) ) {
			return array();
		}

		$normalized = array_map(
			static function ( string $word ): string {
				if ( function_exists( 'mb_strtolower' ) ) {
					return (string) mb_strtolower( $word, 'UTF-8' );
				}
				return strtolower( $word );
			},
			$list
		);

		return array_fill_keys( $normalized, true );
	}

